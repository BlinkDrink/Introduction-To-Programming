#include <iostream>
#include <cmath>

/*
По даденo числo n без използването на цикли да се намери сумата на числата от 1 до n включително.

Пример: n = 5. Тогава сумата е: 1 + 2 + 3 + 4 + 5 = 15

Hint: Колко е сумата на първото с последното число, а на второто и предпоследното?
Как може да се обобщи това свойство?
*/

int main()
{
    int n;
    std::cin >> n;

    int sum = (n * (n + 1)) / 2;
    std::cout << "Sum of the first " << n << " numbers is " << sum;

    return 0;
}
