#include <iostream>

/*
Напишете програма, която при въведено число n и позиция k намира k-тата цифра в десетичния запис на
n от дясно наляво. Считаме, че k е по-малко от дължината на n.

Пример: 
Вход: 123456 4 Изход: 3 
Вход: 1439 1 Изход: 9
*/

int main()
{
    int n, k;
    std::cin >> n >> k;

    int digit = 0;

    for (int i = 0; i < k; i++)
    {
        digit = n % 10;
        n /= 10;
    }

    std::cout << digit;

    return 0;
}
