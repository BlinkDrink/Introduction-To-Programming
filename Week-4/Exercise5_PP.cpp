#include <iostream>

using std::cin;
using std::cout;
using std::endl;

/*
Напишете програма, която прочита едно цяло положително число и извежда на конзолата двоичния му запис наобратно.
(Пояснение: наобратно, защото алгоритъмът, който знаете, дава битовете наобратно).

Пример:
Вход: 15
Изход: 1111

Обяснение:
15dec = 1111bin
*/

int main()
{
    unsigned n;
    cin >> n;

    while (n != 0)
    {
        cout << n % 2;
        n /= 2;
    }

    return 0;
}